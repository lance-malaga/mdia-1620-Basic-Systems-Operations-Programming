// Example 1
// incrementing
var num = 0;
console.log(num); //0

num = num + 1;
console.log(num); //1

num += 1;
console.log(num); //2

num++;
console.log(num); //3

// decrementing
var k = 10;
console.log(k); //10

k = k - 2;
console.log(k); //8

k -= 3;
console.log(k); //5

k--;
console.log(k); //4
